module GeneralHelpers
  module PageCustomMetaHelpers

    def page_sequence
      _current_page_data.sequence
    end

    def url_for_sequence_next
      page_sequence.next
    end

    def url_for_sequence_previous
      page_sequence.previous
    end

    def page_mymeta
      _current_page_data.mymeta
    end


    def page_toc?
      _current_page_data.hide_toc != true
    end

    def page_objectives
      _current_page_data.objectives
    end

    def page_objectives?
      _current_page_data.objectives.present?
    end

  end
end
